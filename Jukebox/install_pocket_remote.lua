local APP_NAME = "pocket_remote_v2"
local APP_PATH = "/pocket_remote_v2.lua"
local APP_URL = "https://raw.githubusercontent.com/NekoSuneProjects/CCTweaks-Scripts-Lua/main/Jukebox/pocket_remote_v2.lua"
local STARTUP_PATH = "/startup.lua"
local BACKUP_PATH = "/startup_pocket_remote_backup.lua"

local function readFile(path)
    if not fs.exists(path) then
        return nil
    end

    local handle = fs.open(path, "r")
    if not handle then
        return nil
    end

    local data = handle.readAll()
    handle.close()
    return data
end

local function writeFile(path, data)
    local handle = fs.open(path, "w")
    if not handle then
        error("Failed to write " .. path)
    end

    handle.write(data)
    handle.close()
end

local function download(url)
    if not http then
        error("HTTP API is disabled")
    end

    local handle, err = http.get(url, nil, true)
    if not handle then
        error("Download failed: " .. tostring(err))
    end

    local data = handle.readAll()
    handle.close()
    return data
end

local function buildStartup()
    return string.format([=[-- Auto-generated by install_pocket_remote.lua
